package com.bypay.domain;

public class CssConfig {
	private String id;	//	ID	BIGINT		8	否	自增长
	private String cssName;	//	CSS_NAME	VARCHAR	30	是	Css样式名
	private String cssUrl;	//	CSS_URL	VARCHAR	200	是	存放地址
	private String cssId;	//	CSS_ID	VARCHAR	100	是	样式编号
	private String reserved1;	//	RESERVED1	VARCHAR	100	是	扩展1
	private String reserved2;	//	RESERVED2	VARCHAR	200	是	扩展2
	private String reserved3;	//	RESERVED3	VARHCAR	300	是	扩展3
	private String remark;	//	REMARK	VARCHAR	200	是	备注
	public String getId() {  
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCssName() {
		return cssName;
	}
	public void setCssName(String cssName) {
		this.cssName = cssName;
	}
	public String getCssUrl() {
		return cssUrl;
	}
	public void setCssUrl(String cssUrl) {
		this.cssUrl = cssUrl;
	}
	public String getCssId() {
		return cssId;
	}
	public void setCssId(String cssId) {
		this.cssId = cssId;
	}
	public String getReserved1() {
		return reserved1;
	}
	public void setReserved1(String reserved1) {
		this.reserved1 = reserved1;
	}
	public String getReserved2() {
		return reserved2;
	}
	public void setReserved2(String reserved2) {
		this.reserved2 = reserved2;
	}
	public String getReserved3() {
		return reserved3;
	}
	public void setReserved3(String reserved3) {
		this.reserved3 = reserved3;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
}
