package com.bypay.domain;

public class RegionCode {
	private String id;				//	ID	BIGINT	8	否	自增长
	private String code;			//	CODE	VARCHAR	10	否	代码
	private String name;			//	NAME	VARCHAR	50	否	名称
	private String superCode;		//	SUPER_CODE	VARCHAR	50	是	上级代码
	private String level;			//	LEVEL	VARCHAR	50	是	级别
	private String remark;			//	REMARK	VARCHAR	50	是	备注
	private String remark1;			//	REMARK1	VARCHAR	200	是	备注
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSuperCode() {
		return superCode;
	}
	public void setSuperCode(String superCode) {
		this.superCode = superCode;
	}
	public String getLevel() {
		return level;
	}
	public void setLevel(String level) {
		this.level = level;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getRemark1() {
		return remark1;
	}
	public void setRemark1(String remark1) {
		this.remark1 = remark1;
	}
	
}
