package com.bypay.domain;

public class SubMerTerminalInfo {
	private String id;	//	ID	INT		11	否	序号，自增长
	private String terminalId;	//	TERMINAL_ID	VARCHAR	25	否	终端号码
	private String subMerId;	//	SUB_MER_ID	BIGINT	11	否	子商户号
	private String tsn;	//	TSN	VARCHAR	30	否	TSN
	private String terminalType;	//	TERMINAL_TYPE	VARCHAR	2	否	费率类型	01：扣率	02：封顶
	private String reserved1;	//	RESERVED1	VARCHAR	200	是	扩展
	private String reserved2;	//	RESERVED2	VARCHAR	200	是	扩展
	private String reserved3;	//	RESERVED3	VARCHAR	200	是	扩展
	
	private String factoryId; //厂商号
	
	public String getFactoryId() {
		return factoryId;
	}
	public void setFactoryId(String factoryId) {
		this.factoryId = factoryId;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTerminalId() {
		return terminalId;
	}
	public void setTerminalId(String terminalId) {
		this.terminalId = terminalId;
	}
	public String getSubMerId() {
		return subMerId;
	}
	public void setSubMerId(String subMerId) {
		this.subMerId = subMerId;
	}
	public String getTsn() {
		return tsn;
	}
	public void setTsn(String tsn) {
		this.tsn = tsn;
	}
	public String getTerminalType() {
		return terminalType;
	}
	public void setTerminalType(String terminalType) {
		this.terminalType = terminalType;
	}
	public String getReserved1() {
		return reserved1;
	}
	public void setReserved1(String reserved1) {
		this.reserved1 = reserved1;
	}
	public String getReserved2() {
		return reserved2;
	}
	public void setReserved2(String reserved2) {
		this.reserved2 = reserved2;
	}
	public String getReserved3() {
		return reserved3;
	}
	public void setReserved3(String reserved3) {
		this.reserved3 = reserved3;
	}
}
