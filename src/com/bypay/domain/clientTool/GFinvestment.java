package com.bypay.domain.clientTool;//

import javax.xml.bind.annotation.XmlAccessType;//
import javax.xml.bind.annotation.XmlAccessorType;//
import javax.xml.bind.annotation.XmlElement;//
import javax.xml.bind.annotation.XmlRootElement;//


@XmlAccessorType(XmlAccessType.FIELD)
@XmlRootElement(name="bppos")
public class GFinvestment extends BeanParent {

	@XmlElement
	private String mobileNum;//手机号
	@XmlElement
	private String legalManName;
	@XmlElement
	private String legalManIdcard;
	@XmlElement
	private String city;    //市信息
	@XmlElement
	private String openBank;  //开户行
	@XmlElement
	private String os;		//操作系统 安卓就传android 苹果就传IOS
	@XmlElement
	private String msgExt;//附加信息
	@XmlElement
	private String transCode;			//服务码
	@XmlElement
	private String cardNo;
	/*
	 * 格式ip|device|os|browser
	例:
	192.168.0.1|pc|windows|ie
	 */
	
	public String getMobileNum() {
		return mobileNum;
	}
	public String getCardNo() {
		return cardNo;
	}
	public void setCardNo(String cardNo) {
		this.cardNo = cardNo;
	}
	public String getTransCode() {
		return transCode;
	}
	public void setTransCode(String transCode) {
		this.transCode = transCode;
	}
	public void setMobileNum(String mobileNum) {
		this.mobileNum = mobileNum;
	}
	public String getLegalManName() {
		return legalManName;
	}
	public void setLegalManName(String legalManName) {
		this.legalManName = legalManName;
	}
	public String getLegalManIdcard() {
		return legalManIdcard;
	}
	public void setLegalManIdcard(String legalManIdcard) {
		this.legalManIdcard = legalManIdcard;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getOpenBank() {
		return openBank;
	}
	public void setOpenBank(String openBank) {
		this.openBank = openBank;
	}
	public String getOs() {
		return os;
	}
	public void setOs(String os) {
		this.os = os;
	}
	public String getMsgExt() {
		return msgExt;
	}
	public void setMsgExt(String msgExt) {
		this.msgExt = msgExt;
	}
	
	
}
