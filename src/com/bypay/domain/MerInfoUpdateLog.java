package com.bypay.domain;

public class MerInfoUpdateLog {
	private String id;			//	ID	BIGINT	20	否	自增长
	private String merId;		//	MER_ID	BIGINT	20	否	商户号
	private String merType;		//	MER_TYPE	SMALLINT	2	否	商户类型
	 //	0机构	1  子商户	2  1级代理	3  2级代理
	private String orgValue;	//	ORG_VALUE	VARCHAR	500	是	原始费率
	private String newValue;	//	NEW_VALUE	VARCHAR	500	是	变更费率
	private String orgTime;		//	ORG_TIME	VARCHAR	25	是	原始费率起始时间
	private String updateTime;	//	UPDATE_TIME	VARCHAR	25	是	变更时间
	private String reserved1;	//	RESERVED1	VARCHAR	200	是	扩展
	private String reserved2;	//	RESERVED2	VARCHAR	200	是	扩展
	private String reserved3;	//	RESERVED3	VARCHAR	LONGVARCHAR	是	扩展

	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMerId() {
		return merId;
	}
	public void setMerId(String merId) {
		this.merId = merId;
	}
	public String getMerType() {
		return merType;
	}
	public void setMerType(String merType) {
		this.merType = merType;
	}
	public String getOrgValue() {
		return orgValue;
	}
	public void setOrgValue(String orgValue) {
		this.orgValue = orgValue;
	}
	public String getNewValue() {
		return newValue;
	}
	public void setNewValue(String newValue) {
		this.newValue = newValue;
	}
	public String getOrgTime() {
		return orgTime;
	}
	public void setOrgTime(String orgTime) {
		this.orgTime = orgTime;
	}
	public String getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}
	public String getReserved1() {
		return reserved1;
	}
	public void setReserved1(String reserved1) {
		this.reserved1 = reserved1;
	}
	public String getReserved2() {
		return reserved2;
	}
	public void setReserved2(String reserved2) {
		this.reserved2 = reserved2;
	}
	public String getReserved3() {
		return reserved3;
	}
	public void setReserved3(String reserved3) {
		this.reserved3 = reserved3;
	}
}
