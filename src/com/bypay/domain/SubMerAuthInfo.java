package com.bypay.domain;

public class SubMerAuthInfo {
	private String authOrderId;//自增长 认证ID
	private String createTime;//创建时间
	private String subMerId;//商户ID
	private String remitType;//认证类型 1实人2实名
	private String realName;//认证人姓名	
	private String idNum;//证件号	
	private String facePicPath;//图片
	private String authStatus;//状态
	private String serialNo;//认证交易流水
	private String terminalId;//终端号码
	
	public String getAuthOrderId() {
		return authOrderId;
	}
	public void setAuthOrderId(String authOrderId) {
		this.authOrderId = authOrderId;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public String getSubMerId() {
		return subMerId;
	}
	public void setSubMerId(String subMerId) {
		this.subMerId = subMerId;
	}
	public String getRemitType() {
		return remitType;
	}
	public void setRemitType(String remitType) {
		this.remitType = remitType;
	}
	public String getRealName() {
		return realName;
	}
	public void setRealName(String realName) {
		this.realName = realName;
	}
	public String getIdNum() {
		return idNum;
	}
	public void setIdNum(String idNum) {
		this.idNum = idNum;
	}
	public String getFacePicPath() {
		return facePicPath;
	}
	public void setFacePicPath(String facePicPath) {
		this.facePicPath = facePicPath;
	}
	public String getAuthStatus() {
		return authStatus;
	}
	public void setAuthStatus(String authStatus) {
		this.authStatus = authStatus;
	}
	public String getSerialNo() {
		return serialNo;
	}
	public void setSerialNo(String serialNo) {
		this.serialNo = serialNo;
	}
	public String getTerminalId() {
		return terminalId;
	}
	public void setTerminalId(String terminalId) {
		this.terminalId = terminalId;
	}
	
	
}
