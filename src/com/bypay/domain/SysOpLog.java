package com.bypay.domain;

public class SysOpLog {

	private String id;
	private String loginName;//管理员ID
	private String opDesc;//操作描述
	private String opUrl;//操作地址
	private String ip;//操作时的IP
	private String ua;//操作时的UA信息
	private String opTime;//操作时间
	private String remark;//备注
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getLoginName() {
		return loginName;
	}
	public void setLoginName(String loginName) {
		this.loginName = loginName;
	}
	public String getOpDesc() {
		return opDesc;
	}
	public void setOpDesc(String opDesc) {
		this.opDesc = opDesc;
	}
	public String getOpUrl() {
		return opUrl;
	}
	public void setOpUrl(String opUrl) {
		this.opUrl = opUrl;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public String getUa() {
		return ua;
	}
	public void setUa(String ua) {
		this.ua = ua;
	}
	public String getOpTime() {
		return opTime;
	}
	public void setOpTime(String opTime) {
		this.opTime = opTime;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
}
